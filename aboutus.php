<p>Vivekanand Education Society was founded in 1959 by Shri. Hashu Advani, along with ten other members, who shared the dream of providing
qualitative education to the youth of our country. In the beginning, Vivekanand Education Society had a very modest launch, with just 256 
students and six classrooms, in the humble barracks of Chembur Camp. But today, it proudly boast of having 3, 75, 000 sq. ft. land, housing
12 buildings and 28 Institutions, ranging from a crèche to Ph.D. Centers. It has over 2000 teaching and non-teaching staff, and more than 18,000 students who pass through its hallowed portals each year.
</p>
<p>Vivekanand Education Society’s Institute of Technology (VESIT) was established in 1984, with the aim of providing professional education in the field of Engineering. This institute is affiliated to the University of Mumbai and follows the rules and regulations laid down by government, AICTE, and University for admission; 51% reserved for Sindhi Linguistic minority and 49% through CAP test. The management quota has been surrendered to DTE to make admission centralized. The institute has a total of 189 faculties, of which 21 are Ph.D. holders and 29 faculty members are pursuing their Ph.D.
</p>
<p>V.E.S.I.T, as on 2017, has been imparting quality education in the field of technology for the last thirty-three years, and is deemed to be one of the most preferred engineering colleges in Mumbai. Students come here to discover an opportunity to explore, to question, to collaborate and improve upon themselves. The high quality of education imparted through various processes of innovative teaching techniques like Process Oriented Guided Inquiry Learning, Peer learning, Flipped Class room and Google class room concept enable the young minds to come together and challenge themselves. The institute postulates on quality education which includes programs like Looking beyond the Syllabus, Skill Enhancement Lectures, Professional Competency Enhancement Programs and so on. In order to meet the increasing demand for a variety of courses, VESIT is perpetually in the process of developing and augmenting its infrastructure and facilities; at the same time upgrading and enhancing its technology. It aims to continuously provide a challenging and vibrant educational atmosphere for its students.
</p>






